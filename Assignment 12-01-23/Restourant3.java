package restourantmanagement;

public class Restourant3 {
	String fname;
	int count;
	double fprice;
	public String getFname() {
		return fname;
	}
	public int getCount() {
		return count;
	}
	public double getFprice() {
		return fprice;
	}
	public Restourant3(String fname, int count, double fprice) {
		super();
		this.fname = fname;
		this.count = count;
		this.fprice = fprice;
	}
	
}



